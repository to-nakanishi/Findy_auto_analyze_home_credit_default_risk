def add_geometric_mean_risk(df: pd.DataFrame) -> pd.DataFrame:
    """
    業種(ORGANIZATION_TYPE)、職種(OCCUPATION_TYPE)、収入タイプ(NAME_INCOME_TYPE)
    の各デフォルト率から幾何平均を算出する。
    ※各カラムに0.001を加算して0による消失を防止
    """
    # 対象となるターゲットエンコーディング後のカラム名
    cols = [
        'ORGANIZATION_TYPE_TARGET_RATE',
        'OCCUPATION_TYPE_TARGET_RATE',
        'NAME_INCOME_TYPE_TARGET_RATE'
    ]

    # すべてのカラムが存在することを確認（FE1での削除等に備え）
    available_cols = [c for c in cols if c in df.columns]

    if len(available_cols) > 0:
        # 各値に0.001を加算して積を計算
        # 幾何平均 = (x1 * x2 * ... * xn) ^ (1/n)
        product = 1.0
        for col in available_cols:
            product *= (df[col] + 0.001)

        df['GEOM_MEAN_RISK'] = (product ** (1 / len(available_cols))).astype(np.float32)

    return df

