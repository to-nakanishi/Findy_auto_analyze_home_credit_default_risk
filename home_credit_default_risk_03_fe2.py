from sklearn.model_selection import KFold

def apply_target_encoding_oof(train_df, test_df, target_cols, target_col='TARGET', n_folds=5, smoothing=10):
    train_res = train_df.copy()
    test_res = test_df.copy()

    kf = KFold(n_splits=n_folds, shuffle=True, random_state=42)
    global_mean = train_res[target_col].mean()

    for col in target_cols:
        new_col = f'{col}_TARGET_RATE'

        # OOF値を格納するNumPy配列を用意（型衝突を避けるため最初はfloat64）
        oof_values = np.zeros(len(train_res), dtype=np.float64)

        # 1. Trainデータへの適用 (OOF)
        for train_idx, val_idx in kf.split(train_res):
            df_t = train_res.iloc[train_idx]
            df_v = train_res.iloc[val_idx]

            # 統計量の計算
            agg = df_t.groupby(col)[target_col].agg(['count', 'mean'])
            counts = agg['count']
            means = agg['mean']

            # Smoothingの適用
            smooth = (means * counts + global_mean * smoothing) / (counts + smoothing)

            # バリデーションデータにマッピング
            oof_values[val_idx] = df_v[col].map(smooth).values

        # 欠損（学習データに存在しないカテゴリ）を全体平均で埋め、型をfloat32に固定
        oof_values = np.where(np.isnan(oof_values), global_mean, oof_values)
        train_res[new_col] = oof_values.astype(np.float32)

        # 2. Testデータへの適用 (全Trainデータを使用)
        agg = train_res.groupby(col)[target_col].agg(['count', 'mean'])
        smooth = (agg['mean'] * agg['count'] + global_mean * smoothing) / (agg['count'] + smoothing)

        test_values = test_res[col].map(smooth).values.astype(np.float64)
        test_values = np.where(np.isnan(test_values), global_mean, test_values)
        test_res[new_col] = test_values.astype(np.float32)

        print(f'Encoded: {new_col}')

    return train_res, test_res

#1-3　返済能力(借入、収入、支出)
# ・ビン別のデフォルト率(CNT_CHILDREN,BU_AMT_CREDIT_SUM_DEBT_latest,POS_CNT_INSTALMENT_FUTURE_latest)
# 　※BU_AMT_CREDIT_SUM_DEBT_latestについてはマイナス値を0とした上でビン別を行う。その際に欠損は欠損として計算すること。
# 　欠損値が24％程度あることから他社債務残高がない場合は欠損となっていると思われる。そのため0で差別化可能と判断。
# 　※リーク対策：Out-of-Fold＋Smoothing)

def apply_binned_target_encoding(train_df, test_df, col_bins_dict, target_col='TARGET', n_folds=5, smoothing=10):

    # 指定されたカラムをビン分割し、ターゲットエンコーディングを適用する関数
    # col_bins_dict: {カラム名: ビンの設定(リストまたは整数)}

    train_res = train_df.copy()
    test_res = test_df.copy()

    kf = KFold(n_splits=n_folds, shuffle=True, random_state=42)
    global_mean = train_res[target_col].mean()

    for col, bins in col_bins_dict.items():
        new_col = f'{col}_BIN_TARGET_RATE'
        temp_bin_col = f'temp_{col}_bin'

        # --- 前処理 (BU_... のマイナス値補正) ---
        t_val = train_res[col].copy()
        v_val = test_res[col].copy()

        if col == 'BU_AMT_CREDIT_SUM_DEBT_latest':
            t_val = t_val.apply(lambda x: 0 if x < 0 else x)
            v_val = v_val.apply(lambda x: 0 if x < 0 else x)

        # --- ビン分割 ---
        # 欠損値はNaNのままカテゴリとして扱われるよう、pd.cutを使用
        train_res[temp_bin_col] = pd.cut(t_val, bins=bins, include_lowest=True, labels=False)
        test_res[temp_bin_col] = pd.cut(v_val, bins=bins, include_lowest=True, labels=False)

        # OOF値を格納するNumPy配列
        oof_values = np.zeros(len(train_res), dtype=np.float64)

        # 1. Trainデータへの適用 (OOF)
        for train_idx, val_idx in kf.split(train_res):
            df_t = train_res.iloc[train_idx]
            df_v = train_res.iloc[val_idx]

            # groupbyのdropna=Falseで欠損値を1つのグループとして扱う
            agg = df_t.groupby(temp_bin_col, dropna=False)[target_col].agg(['count', 'mean'])
            counts = agg['count']
            means = agg['mean']

            smooth = (means * counts + global_mean * smoothing) / (counts + smoothing)

            # mapはNaNもキーとして扱える
            oof_values[val_idx] = df_v[temp_bin_col].map(smooth).values

        # 2. Testデータへの適用
        train_res[new_col] = oof_values.astype(np.float32)

        agg = train_res.groupby(temp_bin_col, dropna=False)[target_col].agg(['count', 'mean'])
        smooth = (agg['mean'] * agg['count'] + global_mean * smoothing) / (agg['count'] + smoothing)

        test_values = test_res[temp_bin_col].map(smooth).values.astype(np.float32)
        test_res[new_col] = test_values

        # 一時的なビンカラムを削除
        train_res = train_res.drop(columns=[temp_bin_col])
        test_res = test_res.drop(columns=[temp_bin_col])

        print(f'Encoded: {new_col}')

    return train_res, test_res

