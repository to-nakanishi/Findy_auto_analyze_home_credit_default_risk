def add_suite_age_interaction(df: pd.DataFrame) -> pd.DataFrame:
    """同行者 × 年齢層ビンの単純結合"""
    # .astype(str) を入れることで、category型の制約を解除して 'Unknown' を入れられるようにする
    suite = df['NAME_TYPE_SUITE'].astype(str).fillna('Unknown')

    age = df['DAYS_BIRTH'] / -365
    age_bin = pd.cut(age, bins=[0, 25, 35, 45, 55, 100],
                     labels=['20', '30', '40', '50', '60']).astype(str)

    # 結合
    df['SUITE_AGE_COMBI'] = suite + "_" + age_bin

    # 改めて category 型にする
    df['SUITE_AGE_COMBI'] = df['SUITE_AGE_COMBI'].astype('category')
    return df

def add_suite_hour_interaction(df: pd.DataFrame) -> pd.DataFrame:
    """同行者 × 直前1時間の照会有無"""
    suite = df['NAME_TYPE_SUITE'].astype(str).fillna('Unknown')
    hour_flag = (df['AMT_REQ_CREDIT_BUREAU_HOUR'].fillna(0) > 0).astype(int).astype(str)

    df['SUITE_HOUR_COMBI'] = suite + "_" + hour_flag
    df['SUITE_HOUR_COMBI'] = df['SUITE_HOUR_COMBI'].astype('category')
    return df

def add_suite_day_interaction(df: pd.DataFrame) -> pd.DataFrame:
    """同行者 × 当日(24h)の照会有無（エラー修正版）"""
    suite = df['NAME_TYPE_SUITE'].astype(str).fillna('Unknown')
    day_flag = (df['AMT_REQ_CREDIT_BUREAU_DAY'].fillna(0) > 0).astype(int).astype(str)

    df['SUITE_DAY_COMBI'] = suite + "_" + day_flag
    df['SUITE_DAY_COMBI'] = df['SUITE_DAY_COMBI'].astype('category')
    return df

