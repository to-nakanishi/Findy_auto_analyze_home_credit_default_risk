def _prep_hour_bin(df: pd.DataFrame) -> pd.Series:
    """HOUR_APPR_PROCESS_START を 0, 1, 2, 3 の4つのビンに分割する補助関数"""
    # 24時間を6時間ごとに区切り、0, 1, 2, 3 というラベルを付ける
    return pd.cut(
        df['HOUR_APPR_PROCESS_START'],
        bins=[0, 6, 12, 18, 24],
        labels=['0', '1', '2', '3'],
        include_lowest=True
    ).astype(str)

def add_org_hour_interaction(df: pd.DataFrame) -> pd.DataFrame:
    """業種 × 申請時間ビン"""
    hour_bin = _prep_hour_bin(df)
