def add_credit_income_ratio(df: pd.DataFrame) -> pd.DataFrame:
    # 融資額 / 収入
    income_safe = df['AMT_INCOME_TOTAL'].replace(0, np.nan)
    df['CREDIT_INCOME_RATIO'] = (df['AMT_CREDIT'] / income_safe).astype(np.float32)
    return df

def add_annuity_income_ratio(df: pd.DataFrame) -> pd.DataFrame:
    # ローンの年間負債額 / 収入
    income_safe = df['AMT_INCOME_TOTAL'].replace(0, np.nan)
    df['ANNUITY_INCOME_RATIO'] = (df['AMT_ANNUITY'] / income_safe).astype(np.float32)
    return df

def add_credit_annuity_ratio(df: pd.DataFrame) -> pd.DataFrame:
    # 完済年数
    annuity_safe = df['AMT_ANNUITY'].replace(0, np.nan)
    df['CREDIT_ANNUITY_RATIO'] = (df['AMT_CREDIT'] / annuity_safe).astype(np.float32)
    return df

def add_finish_age(df: pd.DataFrame) -> pd.DataFrame:
    # 完済時の年齢
    annuity_safe = df['AMT_ANNUITY'].replace(0, np.nan)
    loan_term_years = df['AMT_CREDIT'] / annuity_safe
    current_age_years = -df['DAYS_BIRTH'] / 365
    df['FINISH_AGE'] = (loan_term_years + current_age_years).astype(np.float32)
    return df

