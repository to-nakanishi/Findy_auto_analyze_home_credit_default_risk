# --- 追加カラムに特化したランキング表示コード ---

def show_added_features_ranking(models_lgb, models_cat, feature_names):
    # 1. LGBMの寄与度(%)とランク
    lgb_imp = np.mean([m.feature_importance(importance_type='gain') for m in models_lgb], axis=0)
    lgb_pct = (lgb_imp / np.sum(lgb_imp)) * 100

    # 2. CatBoostの寄与度(%)とランク
    cat_imp = np.mean([m.get_feature_importance() for m in models_cat], axis=0)
    cat_pct = (cat_imp / np.sum(cat_imp)) * 100

    # データフレーム化
    df = pd.DataFrame({
        'feature': feature_names,
        'LGBM_pct': lgb_pct,
        'CAT_pct': cat_pct
    })

    # 全体での順位を計算
    df['LGBM_Rank'] = df['LGBM_pct'].rank(ascending=False).astype(int)
    df['CAT_Rank'] = df['CAT_pct'].rank(ascending=False).astype(int)

    # 追加カラムのキーワードでフィルタリング
    added_keys = ['abnormal_MAX_frag', '_TE', '_mean', 'border', 'round', 'geometric']
    added_df = df[df['feature'].str.contains('|'.join(added_keywords))].copy()

    # 表示用に整形（CATの順位が高い順に並べる）
    added_df = added_df.sort_values(by='CAT_Rank')

    print(f"=== 追加カラム限定：寄与度(%) & 全体順位 (全{len(feature_names)}列中) ===")

    # カラム名をわかりやすくリネーム
    result = added_df[[
        'feature', 'LGBM_pct', 'LGBM_Rank', 'CAT_pct', 'CAT_Rank'
    ]].reset_index(drop=True)

    # 表形式で表示
    print(result.to_markdown(index=False))

    # 簡易サマリー
    top100_lgb = len(result[result['LGBM_Rank'] <= 100])
    top100_cat = len(result[result['CAT_Rank'] <= 100])
    print(f"\n[Summary]")
    print(f"TOP 100入りした追加カラム: LGBM {top100_lgb}個 / CatBoost {top100_cat}個")

