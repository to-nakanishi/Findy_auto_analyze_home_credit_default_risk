from sklearn.model_selection import KFold

def apply_ext_score_bin_te(train_df, test_df, target_col='TARGET', n_folds=5, smoothing=10):
    train_res = train_df.copy()
    test_res = test_df.copy()

    col = 'EXT_SOURCES_CUSTOM_MEAN'
    new_col = 'EXT_SOURCES_BIN_TARGET_RATE'

    bins = np.arange(0, 1.05, 0.05)

    train_res['ext_bin'] = pd.cut(train_res[col], bins=bins, include_lowest=True, labels=False)
    test_res['ext_bin'] = pd.cut(test_res[col], bins=bins, include_lowest=True, labels=False)

    kf = KFold(n_splits=n_folds, shuffle=True, random_state=42)
    global_mean = train_res[target_col].mean()

    oof_values = np.zeros(len(train_res), dtype=np.float64)

    for train_idx, val_idx in kf.split(train_res):
        df_t = train_res.iloc[train_idx]
        df_v = train_res.iloc[val_idx]

        agg = df_t.groupby('ext_bin')[target_col].agg(['count', 'mean'])
        counts = agg['count']
        means = agg['mean']
        smooth = (means * counts + global_mean * smoothing) / (counts + smoothing)

        oof_values[val_idx] = df_v['ext_bin'].map(smooth).values

    oof_values = np.where(np.isnan(oof_values), global_mean, oof_values)
    train_res[new_col] = oof_values

    agg = train_res.groupby('ext_bin')[target_col].agg(['count', 'mean'])
    smooth = (agg['mean'] * agg['count'] + global_mean * smoothing) / (agg['count'] + smoothing)
    test_values = test_res['ext_bin'].map(smooth).values.astype(np.float64)
    test_values = np.where(np.isnan(test_values), global_mean, test_values)
    test_res[new_col] = test_values

    train_res = train_res.drop(columns=['ext_bin'])
    test_res = test_res.drop(columns=['ext_bin'])

    return train_res, test_res

