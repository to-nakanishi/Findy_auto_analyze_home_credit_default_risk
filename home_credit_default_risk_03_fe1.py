def apply_custom_ext_mean(df):

    # EXT_SOURCE_1～3の特殊補正平均
    # 分子：1〜3の合計（欠損は0扱い）
    # 分母：2 + (EXT_2が存在すれば1、なければ0)

    # 欠損を0に置換して合計を計算
    ext_sum = df[['EXT_SOURCE_1', 'EXT_SOURCE_2', 'EXT_SOURCE_3']].fillna(0).sum(axis=1)

    # 分母の計算: EXT_SOURCE_2が欠損していなければ1、していれば0を返す
    ext2_notnull = df['EXT_SOURCE_2'].notnull().astype(int)
    denominator = 2 + ext2_notnull

    # 特殊平均の算出
    df['EXT_SOURCES_CUSTOM_MEAN'] = ext_sum / denominator

    return df

