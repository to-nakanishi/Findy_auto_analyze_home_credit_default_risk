#4-2　FE(試走結果に基づくEDA(第2有力指標))　キリ番(AMT_CREDIT,AMT_GOODS_PRICE)
def apply_round_number_flags(df):
    """
    AMT_CREDIT, AMT_GOODS_PRICE がキリの良い数字かどうかを判定
    1万, 10万, 100万で割り切れる（小数点以下がない）場合にフラグを立てる
    """
    cols = ['AMT_CREDIT', 'AMT_GOODS_PRICE']
    divisors = [10000, 100000, 1000000] # 1e4, 1e5, 1e6

    for col in cols:
        if col not in df.columns:
            continue

        for div in divisors:
            # 除算して余りが0（または非常に小さい誤差範囲）ならフラグ
            # divの大きさに合わせて 1e4なら "10k", 1e5なら "100k" のように命名
            label = f"{div/1000:g}k"
            new_col = f"{col}_is_round_{label}"

            # 欠損値は 0 (False) と判定されるように処理
            df[new_col] = (df[col] % div == 0).astype(int)

    return df

