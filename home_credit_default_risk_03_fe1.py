def apply_abnormal_flags(train_df, test_df, cols):
    # 1. 必要なカラムだけ抽出（存在しない場合は無視）
    target_cols = [c for c in cols if c in train_df.columns]

    # 2. フラグだけを格納する辞書
    train_new_flags = {}
    test_new_flags = {}

    for col in target_cols:
        # 計算時だけ float64 として扱う
        series_train = train_df[col].astype(float)
        q95_train = series_train.quantile(0.95)

        new_col_name = f'{col}_abnormal_MAX_frag'
        divisor = q95_train + 1e-9

        # 判定後、即座に int8 (メモリ最小) に変換
        train_new_flags[new_col_name] = (series_train / divisor > 100).astype('int8')
        test_new_flags[new_col_name] = (test_df[col].astype(float) / divisor > 100).astype('int8')

    # 3. 最後に一括結合（メモリ負荷を最小化）
    train_df = pd.concat([train_df, pd.DataFrame(train_new_flags, index=train_df.index)], axis=1)
    test_df = pd.concat([test_df, pd.DataFrame(test_new_flags, index=test_df.index)], axis=1)

    return train_df, test_df
