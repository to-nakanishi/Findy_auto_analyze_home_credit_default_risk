def apply_ext_source_geometric_mean(train_df, test_df):
    """
    EXT_SOURCE_1～3の幾何平均（3乗根）を生成
    欠損時は、NaNデータのデフォルト率に基づいた特定スコアで補完
    """
    train_res = train_df.copy()
    test_res = test_df.copy()

    # 調査結果に基づく補完値の設定
    fill_values = {
        'EXT_SOURCE_1': 0.3750,
        'EXT_SOURCE_2': 0.5250,
        'EXT_SOURCE_3': 0.3750
    }

    ext_cols = ['EXT_SOURCE_1', 'EXT_SOURCE_2', 'EXT_SOURCE_3']

    for col in ext_cols:
        fv = fill_values[col]
        # 欠損値を指定のスコアで補完した一時的なカラムを作成
        train_res[f'{col}_filled'] = train_res[col].fillna(fv)
        test_res[f'{col}_filled'] = test_res[col].fillna(fv)

    # 幾何平均の計算（3つの乗算の1/3乗）
    train_res['EXT_SOURCES_GEOM_MEAN'] = (
        train_res['EXT_SOURCE_1_filled'] * train_res['EXT_SOURCE_2_filled'] * train_res['EXT_SOURCE_3_filled']
    ) ** (1/3)

    test_res['EXT_SOURCES_GEOM_MEAN'] = (
        test_res['EXT_SOURCE_1_filled'] * test_res['EXT_SOURCE_2_filled'] * test_res['EXT_SOURCE_3_filled']
    ) ** (1/3)

    # 作業用カラムの削除
    drop_cols = [f'{col}_filled' for col in ext_cols]
    train_res = train_res.drop(columns=drop_cols)
    test_res = test_res.drop(columns=drop_cols)

    return train_res, test_res

